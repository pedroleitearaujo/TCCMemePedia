    <h1 class="ttlNormasDeConduta">Normas de conduta</h1>
    <p class="txt">
    Uma das coisas mais importantes que deve saber é que, para mantermos o nosso objetivo, 
    nós necessitamos seguir algumas normas que lhe pedimos humildemente para respeitar.
    Tentamos não discutir; este não é um fórum de discussão. Todos tentam chegar a um 
    consenso sobre o que deve estar escrito nos verbetes, mas procuramos não alongar as 
    discussões.
    Tentamos fazer com que os verbetes tenham a maior imparcialidade possível, o que
     faz com que mesmo os artigos sobre temas controversos não sejam concebidos como 
     plataforma para qualquer pregação. Lembre-se que está a colaborar numa enciclopédia, 
     o que significa que há muita coisa que ela não é. Os verbetes devem ser de leitura 
     fácil e oferecer explicações claras, não servir de panfletos de propaganda.
    Caso se sinta constrangido por alterar o trabalho de outrem e pretender dar a sua 
    opinião, questionar ou tecer comentários sobre um artigo, use a página de discussão 
    do artigo, tendo em conta o que é dito acima.
    Nada de linguagem de baixo calão ou preconceito de qualquer tipo.
    Talvez queira rever as coisas a não fazer na Memepédia. Existem outras normas 
    comunitárias codificadas através das ligações listadas abaixo, particularmente 
    a política da Memepédia.
